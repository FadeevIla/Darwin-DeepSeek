def get_help_text(commands: list) -> str:
    """Генерирует /help из списка команд."""
    lines = ["🐉 <b>УРОБОРОС — Команды</b>\n"]
    for cmd, desc in commands:
        lines.append(f"{cmd} — {desc}")
    return "\n".join(lines)